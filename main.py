def _load_sites_db():
    """Load the 349-site database from JSON, generated by convert_pvsyst.py."""
    import json as _json
    import unicodedata
    db = {}
    if os.path.exists(_SITES_DB_PATH):
        with open(_SITES_DB_PATH, 'r', encoding='utf-8') as f:
            raw = _json.load(f)
        for name, entry in raw.items():
            norm_name = unicodedata.normalize('NFC', name)
            db[norm_name] = {
                'lat': entry['lat'],
                'lon': entry['lon'],
                'alt': entry.get('alt', 0) or 0,
                'radio_km': entry.get('radio_km', 15),
                'pvsyst_file': None,  # resolved dynamically
                'csv_file': entry.get('csv_file'),
                'hr': entry.get('hr_monthly'),
                'fuente_meteo': entry.get('fuente_meteo', 'Meteonorm 8.2'),
                'global_h_monthly': entry.get('global_h_monthly'),
                't_amb_monthly': entry.get('t_amb_monthly'),
            }
    return db


COMUNAS_DB = _load_sites_db()


def find_pvsyst_file(site_name=None):
    """Busca el archivo CSV PVsyst para un sitio dado."""
    if site_name and site_name in COMUNAS_DB:
        csv_file = COMUNAS_DB[site_name].get('csv_file')
        if csv_file:
            path = os.path.join(_PVSYST_DIR, csv_file)
            if os.path.exists(path):
                return path
    # Fallback: búsqueda case-insensitive
    if site_name:
        import unicodedata
        norm = unicodedata.normalize('NFC', site_name).lower()
        for name, info in COMUNAS_DB.items():
            if unicodedata.normalize('NFC', name).lower() == norm:
                csv_file = info.get('csv_file')
                if csv_file:
                    path = os.path.join(_PVSYST_DIR, csv_file)
                    if os.path.exists(path):
                        return path
    # Fallback legacy paths
    legacy = {
        "Curacavi": [os.path.join(_BASE, "data", "Curacavi.CSV")],
        "Talhuen": [os.path.join(_BASE, "data", "Talhuen san vicente.CSV")],
    }
    if site_name and site_name in legacy:
        for p in legacy[site_name]:
            if os.path.exists(p):
                return p
    return None


def haversine_km(lat1, lon1, lat2, lon2):
    """Distancia en km entre dos puntos geograficos."""
    R = 6371
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = math.sin(dlat/2)**2 + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2)) * math.sin(dlon/2)**2
    return R * 2 * math.asin(math.sqrt(a))


def find_nearest_comuna(lat, lon, max_km=50):
    """Encuentra el sitio PVsyst mas cercano.
    Siempre retorna el más cercano si está dentro de max_km.
    Retorna (nombre, info, distancia_km) o None."""
    best = None
    best_dist = float('inf')
    for nombre, info in COMUNAS_DB.items():
        d = haversine_km(lat, lon, info['lat'], info['lon'])
        if d < best_dist:
            best = (nombre, info, d)
            best_dist = d
    # Retornar el más cercano si está dentro de max_km
    if best and best_dist <= max_km:
        return best
    return None


def reverse_geocode_comuna(lat, lon):
    """Identifica la comuna chilena a partir de coordenadas usando Nominatim (OSM).
    Retorna el nombre de la comuna o None si falla."""
    import urllib.request
    import json as _json
    try:
        url = (f"https://nominatim.openstreetmap.org/reverse?"
               f"lat={lat}&lon={lon}&format=json&addressdetails=1&zoom=10"
               f"&accept-language=es")
        req = urllib.request.Request(url, headers={'User-Agent': 'AgroclimaticoCL/1.0'})
        with urllib.request.urlopen(req, timeout=5) as resp:
            data = _json.loads(resp.read().decode())
        addr = data.get('address', {})
        # Nominatim returns comuna in different fields depending on the area
        comuna = (addr.get('city') or addr.get('town') or
                  addr.get('municipality') or addr.get('village') or
                  addr.get('county') or '')
        return comuna.strip() if comuna else None
    except Exception:
        return None


def find_predial_comuna(lat, lon):
    """Busca la comuna para el informe predial. Usa reverse geocoding
    y luego valida contra las comunas disponibles en la BD."""
    if not PREDIAL_AVAILABLE:
        return None

    comuna_name = reverse_geocode_comuna(lat, lon)
    if not comuna_name:
        return None

    # Verificar si existe en la BD (busqueda flexible)
    available = get_available_comunas()
    # Match exacto
    if comuna_name in available:
        return comuna_name
    # Match case-insensitive
    for c in available:
        if c.upper() == comuna_name.upper():
            return c
    # Match parcial (la comuna podria venir como "San Vicente de Tagua Tagua"
    # pero en la BD ser "San Vicente")
    for c in available:
        if c.upper() in comuna_name.upper() or comuna_name.upper() in c.upper():
            return c
    # No match -- retornar el nombre detectado de todas formas
    return comuna_name


